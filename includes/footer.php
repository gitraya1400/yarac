    <script>
    // Enhanced Footer JavaScript
    document.addEventListener('DOMContentLoaded', function() {
        initializeFooter();
    });

    function initializeFooter() {
        // Newsletter form submission
        const footerNewsletterForm = document.getElementById('footer-newsletter-form');
        if (footerNewsletterForm) {
            footerNewsletterForm.addEventListener('submit', function(e) {
                e.preventDefault();
                
                const formData = new FormData(this);
                const button = this.querySelector('button');
                const originalHTML = button.innerHTML;
                
                // Show loading state
                button.innerHTML = '<i class="fas fa-spinner fa-spin"></i>';
                button.disabled = true;
                
                fetch('api/newsletter.php', {
                    method: 'POST',
                    body: formData
                })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        showNotification('Thank you for subscribing to our newsletter!', 'success');
                        this.reset();
                        
                        // Success animation
                        button.innerHTML = '<i class="fas fa-check"></i>';
                        button.style.background = 'linear-gradient(135deg, #27ae60, #2ecc71)';
                        
                        setTimeout(() => {
                            button.innerHTML = originalHTML;
                            button.style.background = '';
                            button.disabled = false;
                        }, 2000);
                    } else {
                        showNotification(data.message || 'Subscription failed', 'error');
                        button.innerHTML = originalHTML;
                        button.disabled = false;
                    }
                })
                .catch(error => {
                    showNotification('An error occurred', 'error');
                    button.innerHTML = originalHTML;
                    button.disabled = false;
                });
            });
        }

        // Social media hover effects
        const socialIcons = document.querySelectorAll('.social-icon');
        socialIcons.forEach(icon => {
            icon.addEventListener('mouseenter', function() {
                const platform = this.getAttribute('data-platform');
                if (platform) {
                    this.setAttribute('title', `Follow us on ${platform}`);
                }
            });
        });

        // Smooth scroll for back to top
        const backToTop = document.querySelector('.back-to-top');
        if (backToTop) {
            // Show/hide back to top button based on scroll position
            window.addEventListener('scroll', function() {
                if (window.pageYOffset > 300) {
                    backToTop.style.opacity = '1';
                    backToTop.style.visibility = 'visible';
                } else {
                    backToTop.style.opacity = '0';
                    backToTop.style.visibility = 'hidden';
                }
            });
        }

        // Add floating animation to shapes
        const shapes = document.querySelectorAll('.floating-shape');
        shapes.forEach((shape, index) => {
            shape.style.animationDelay = `${index * 2}s`;
        });

        // Payment method tooltips
        const paymentIcons = document.querySelectorAll('.payment-icon');
        paymentIcons.forEach(icon => {
            icon.addEventListener('mouseenter', function() {
                const title = this.getAttribute('title');
                if (title) {
                    // Create tooltip
                    const tooltip = document.createElement('div');
                    tooltip.className = 'payment-tooltip';
                    tooltip.textContent = title;
                    tooltip.style.cssText = `
                        position: absolute;
                        bottom: 100%;
                        left: 50%;
                        transform: translateX(-50%);
                        background: rgba(0, 0, 0, 0.8);
                        color: white;
                        padding: 5px 10px;
                        border-radius: 4px;
                        font-size: 0.8rem;
                        white-space: nowrap;
                        z-index: 1000;
                        margin-bottom: 5px;
                    `;
                    
                    this.style.position = 'relative';
                    this.appendChild(tooltip);
                }
            });
            
            icon.addEventListener('mouseleave', function() {
                const tooltip = this.querySelector('.payment-tooltip');
                if (tooltip) {
                    tooltip.remove();
                }
            });
        });
    }

    function scrollToTop() {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    }

    // Add intersection observer for footer animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const footerObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate-in');
                
                // Stagger animation for footer sections
                const sections = entry.target.querySelectorAll('.footer-section');
                sections.forEach((section, index) => {
                    setTimeout(() => {
                        section.style.opacity = '1';
                        section.style.transform = 'translateY(0)';
                    }, index * 200);
                });
            }
        });
    }, observerOptions);

    // Observe footer
    const footer = document.querySelector('.footer');
    if (footer) {
        footerObserver.observe(footer);
        
        // Initially hide sections for animation
        const sections = footer.querySelectorAll('.footer-section');
        sections.forEach(section => {
            section.style.opacity = '0';
            section.style.transform = 'translateY(30px)';
            section.style.transition = 'all 0.6s ease';
        });
    }
    </script>

